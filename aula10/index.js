/*
Aritiméticos
+ adição / concatenação
- subtração
/ divisão
** potenciação
% resto da divisão
incremento = ++
decremento = --


*/ 

const num = 20;
const num1 = 40;
console.log(num + num1); // adição
console.log(num / num1); // divisão
console.log(num * num1); // multiplicação
console.log(num ** num1); // Potenciação
console.log(num % num1); // resto da divisão


